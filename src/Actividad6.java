public class Actividad6 {
    public static void main(String[] args) {
        //Declaramos la variable nuemro para poder hacer un contador haci atras desde 3960 hasta 160.
        int numero =320;
        //Declaramaso el buble do/while para poder mostrar el contador hacia atras
        do {
            System.out.println(numero);
            numero=numero-20;
        } while (numero>=160);
    }
}
